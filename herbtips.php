	<h2><b><u>HARVESTING</u></b></h2>
		<div id="herbs">
			<img src="harvest.jpg" alt="Harvesting Herbs" style="width:250px;height:220px;">
		</div>
	<br>
	<p><b>Herbs should be harvested when the oils responsible for their flavor and aroma are at their highest.
	      Proper timing depends on the plant part you are harvesting and how you plan to use it. 
	      Herbs can be harvested when the plant has enough foliage to maintain growth.</b></p>
		  <br>
	<p><b>Try to harvest early in the day after plants dry off and before it gets hot. 
	      Herbs are best harvested before they start to flower otherwise leaf production declines. 
		  Deadheading or removal of flowers as they appear will result in the continued production of new leaves suitable for harvest.</b></p>
		  
	<h2><b><u>DRYING</u></b></h2>
		<div id="herbs">
			<img src="drying.jpg" alt="Drying Herbs" style="width:250px;height:220px;">
		</div>
	<br>
	<p><b>The traditional way to preserve herbs is by air drying or using low heat. 
	      Drying concentrates the flavor of herbs so you may need to only use one-third to one-fourth the amount of fresh herbs in recipes.
		  Tie the herbs in loose bunches that allow for good air circulation around each bunch.  
		  Hang the herb bunches in a warm, dry, well-ventilated area out of the sun.  
		  A garage, shed, barn or well ventilated attic work well. It may take up to a month for herbs to dry completely.</b></p>
		  <br>
	<p><b><li><i>Tray Drying</i></li> 
		  Requires a simple frame that has screen wire fastened to the bottom works well. 
	      Put herbs in a single layer across the bottom and place the trays in a warm, well-ventilated area out of the sun. 
		  Leaves may need to be turned to insure even drying.</b></p>
		  <br>
	<p><b><li><i>Drying with Heat</i></li> 
	      Can involve the use of conventional ovens, micro-wave ovens or dehydrating ovens.</b></p>
		  <br>
	<p><b><li><i>Home food dehydrators</i></li> do an excellent job of drying herbs. Refer to the ownerâ€™s manual for specifics and settings.</b></p>
		  <br>
	<p><b><li><i>Oven Drying</i></li> 
	      Works well if the temperatures can be closely monitored. 
	      Oftentimes, oven temperatures cannot be set low enough and the result is loss of flavor and color. 
		  However, if oven temperatures can be held between 90 and 110 degrees that would be the ideal oven drying temperature. 
		  Setting your oven at the lowest temperature and leaving the oven door slightly open often results in temperatures that can be maintained at that level. 
		  Check the progress of drying often and turn the herbs if necessary. It may take 3-4 hours to dry herbs using this method.</b></p>
		  <br>
	<p><b><li><i>Microwave Ovens</i></li> 
	      Can also be used to dry small quantities of herbs quickly. 
		  Always observe safety precautions when drying herbs in a microwave and check manufacture's recommendations for using their product 
		  when drying herbs as the risk of scorching herbs and the possibility of starting fires exists.</b></p>
	      
	<h2><b><u>FREEZING</u></b></h2>
		<div id="herbs">
			<img src="freeze.jpg" alt="Freezing Herbs" style="width:250px;height:220px;">
		</div>
	<br>
	<p><b>While freezing herbs is perhaps the easiest method, herbs handled this way are most useful used in the cooking process, as frozen herbs are not suitable for garnish. 
	      Freezing will alter the appearance quality but not the flavor quality.  
          After herbs are washed, they can be coarsely chopped and generous pinches of herbs can be placed in water filled ice cube trays and frozen. 
		  The cubes can then be transferred to plastic bags and placed in the freezer. Individual cubes can be taken out as needed.
          Herb leaves can also be blanched in boiling water for about one minute and then quickly cooled by plunging them into ice water. 
		  The leaves can then be put into tightly seal plastic bags and frozen.</b></p>
